//Object with a property (key:value)...........2
let person = {
    Name : 'Amanda',
    age : 40,
};
console.log(person);

// Dot notation....a way to change the key value data
person.Name = 'Peter';
console.log(person.Name);
//Bracket notation... a way also to change key value data
person['Name'] = 'Ada';
console.log(person.Name);


